<h1>Item Types</h1>

<p>This part of the website manages item types.  An item type is a sort of
template for a class of items, such as a bike or a carabiner.  The item type
defines a set of features that the item is expected to have.  For example, a
rope has a length; a fuel tank has a fullness, and so forth.</p>

