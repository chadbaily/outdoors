adventure attendees will be able to view your contact information!</p> 
{:ERROR}

{INSTRUCTIONS:}

<p>Follow this process to create an adventure:</p>

<ol>
  <li>Create the adventure with the form below.  The adventure isn't visible at
  this point.</li>
  <li>Add questions to it.</li>
  <li>Join it yourself.</li>
  <li>If you want, you can ask the officers to inspect it before you activate it.</li>
  <li>Activate it (it is now visible to members).</li>
  <li>(Optional) announce it to the mailing list.</li>
</ol>

<p>Be sure to put full details into the adventure's description.  You should
let your attendees know what to bring and what the club will provide, and
anything else they need to be prepared.</p>

<p class="notice">Don't paste text from a word processor.  It'll insert funny
characters that won't come out right.  If you want to draft your description,
use Notepad or another plain-text editor.</p>

<p class="notice">Look for the locations you need in the pulldown menus below.
If you don't see them, click on the link to create a new location, <b>then
refresh this page</b>.</p>

{:INSTRUCTIONS}

