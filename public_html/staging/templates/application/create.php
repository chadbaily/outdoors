<p>
  All officers work together to improve the club and decide on club
  policies.  There is generally a lot of e-mail that gets sent to the
  officers list, and as an officer you must be willing to read and respond
  to these e-mails.  A few officers handle money, some lead more trips than
  others, and some deal with gear more than others.  The roles are flexible,
  but we all put a lot of energy into the club.  You really don't need to be an expert in an outdoor activity.
  The single most important aspect that we look for in new officers is a passion for the Club.
</p>

<p>
  Some of the benefits of being an officer include:
  <ul>
    <li>Gaining leadership, management, and budgeting experience.</li>
    <li>Interacting with famous outdoor equipment manufacturers; some provide significant discounts on gear.</li>
    <li>Wilderness First Aid training.</li>
    <li>Promoting the outdoors among students and community members.</li>
    <li>Being a part of one of the largest CIOs on campus.</li>
    <li>Having fun!!!</li>
  </ul>
